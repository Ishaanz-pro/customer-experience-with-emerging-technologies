# Generate smart AI-style response
def generate_response(q):
    q = q.lower()
    if "under" in q:
        return "ğŸ’¸ Great! Let me find budget-friendly options for you."
    if "gym" in q:
        return "ğŸ‹ï¸â€â™€ï¸ Looking for gym gear? You're in shape!"
    if "eco" in q or "recycled" in q:
        return "ğŸŒ¿ Awesome! You care about the planet!"
    return "ğŸ›ï¸ Here's what I think you'll like."

# Filter products based on query
def filter_products(q):
    q = q.lower()
    result = df.copy()
    if "gym" in q:
        result = result[result["description"].str.lower().str.contains("gym")]
    if "red" in q:
        result = result[result["color"].str.lower().str.contains("red")]
    for amt in [1000, 1500, 2000, 2500, 3000]:
        if f"under {amt}" in q:
            result = result[result["numeric_price"] <= amt]
            break
    return result.head(9)

# Display interaction
