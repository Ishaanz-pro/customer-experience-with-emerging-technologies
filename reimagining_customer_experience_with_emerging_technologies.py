# Price range slider
min_price, max_price = st.slider("Select your price range:", 0, 5000, (0, 3000))

# Simulate AI Response
def generate_response(q):
    q = q.lower()
    if "budget" in q or "under" in q:
        return "ğŸ§¾ Here are some budget-friendly options:"
    elif "gym" in q:
        return "ğŸ’ª These products are great for gym and fitness training!"
    elif "eco" in q or "recycled" in q:
        return "ğŸŒ± Here are eco-friendly products just for you:"
    else:
        return "ğŸ¯ Here are some smart recommendations based on your input:"

# Filter logic
def filter_products(q):
    q = q.lower()
    result = df.copy()
    if "gym" in q:
        result = result[result["description"].str.lower().str.contains("gym")]
    if "red" in q:
        result = result[result["color"].str.lower().str.contains("red")]
    if "under" in q:
        result = result[result["numeric_price"] < max_price]
    return result.head(9)

# Handle user input
